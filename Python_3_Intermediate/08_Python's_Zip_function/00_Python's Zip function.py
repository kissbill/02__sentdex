#Python's Zip function ################################

x = [1,2,3,4]
y = [ 12,3,35,22]
z = [ 'a','b','c','d']
################################
for a,b,c in zip(x,y,z):
	print(a,b,c)

#1 12 s
#2 3 h
#3 35 dd
#4 22 w
################################
print(zip(x,y,z))
#<zip object at 0x0000021C1B6A3BC8>

################################
for i in zip(x,y,z):
	print(i)
#(1, 12, 's')
#(2, 3, 'h')
#(3, 35, 'dd')
#(4, 22, 'w')

################################################################
#making a list for them ################################
print(list(zip(x,y,z)))

#[(1, 12, 's'), (2, 3, 'h'), (3, 35, 'dd'), (4, 22, 'w')]

#creating a dictionary only two array ################################
print(dict(zip(x,y)))

#{1: 12, 2: 3, 3: 35, 4: 22}
################################################################

[print(a,b,c) for a , b ,c in zip(x,y,z)]